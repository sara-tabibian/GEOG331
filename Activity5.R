#assigning DOY ranges to seasons
datD$season <- with(datD, ifelse(doy %in% 1:59 | doy %in% 335:365, "Winter", 
                                 ifelse(doy %in% 60:151, "Spring",
                                        ifelse(doy %in% 152:243, "Summer", "Fall"))))

#filtering data for each year

dat2016 <- subset(datD, year == 2016)
dat2017 <- subset(datD, year == 2017)

#plot for 2016

ggplot(dat2016, aes(x = season, y = discharge)) + 
  geom_violin(fill = "blue", color = "black") +
  labs(title = "discharge by season (2016)", x = "season", y = "Discharge ft^3/sec") +
  theme_minimal()

